# Old style formatting
print('Old style : Hello, %s' % 'you!')

# Old style formatting - multiple vars
print('Old style : Hello, %s and %s' % ('you', 'you!'))

# Newer formatting style

print('Newer style : Hello, {}'.format('you!'))

# Newest : Formatted String Literals

name =  'you'
print(f'Formatted String Literals : Hello, {name}')
